import { TransportAdapter } from './transport-adapter.js';

/**
 * WsTransport - WebSocket-backed TransportAdapter for the Wavelog Worker.
 *
 * Responsibilities:
 *   - Establish and maintain a WebSocket connection to the Worker
 *   - Authenticate on the assigned topic (HMAC token)
 *   - On incoming push: notify listeners so the SyncEngine can react immediately
 *
 * send() delegates to the provided AjaxTransport — the Worker is receive-only,
 * all writes still go through the Ajax heartbeat endpoint.
 *
 * Usage:
 *   const ws = new WsTransport(ajaxTransport, cfg.url, cfg.topic, cfg.token);
 *   ws.onPush = (payload) => syncEngine.triggerNow();
 *   ws.connect();
 */
export class WsTransport extends TransportAdapter {
	/**
	 * @param {TransportAdapter} ajaxTransport - Fallback for send()
	 * @param {string} url   - Worker base URL, e.g. "ws://localhost:9000"
	 * @param {string} topic - Topic to subscribe to, e.g. "contest_session.abc123"
	 * @param {string} token - HMAC auth token generated by PHP
	 */
	constructor(ajaxTransport, url, topic, token) {
		super();
		this._ajax   = ajaxTransport;
		this._url    = url.replace(/\/$/, '');
		this._topic  = topic;
		this._token  = token;
		this._ws     = null;
		this._ready  = false;

		/** Called with the raw payload object on every incoming push frame. */
		this.onPush  = null;
	}

	/**
	 * Open the WebSocket connection and authenticate.
	 * Safe to call multiple times — ignores if already connected.
	 */
	connect() {
		if (this._ws && this._ws.readyState <= WebSocket.OPEN) {
			return;
		}

		const wsUrl = this._url + '/ws?topic=' + encodeURIComponent(this._topic);
		this._ws    = new WebSocket(wsUrl);
		this._ready = false;

		this._ws.addEventListener('open', () => {
			this._ws.send(JSON.stringify({ type: 'auth', token: this._token }));
		});

		this._ws.addEventListener('message', (event) => {
			let msg;
			try {
				msg = JSON.parse(event.data);
			} catch {
				console.warn('[WsTransport] Unparseable frame:', event.data);
				return;
			}

			switch (msg.type) {
				case 'auth_ok':
					this._ready = true;
					console.info('[WsTransport] authenticated on topic:', this._topic);
					break;
				case 'push':
					if (typeof this.onPush === 'function') {
						this.onPush(msg.payload);
					}
					break;
				case 'error':
					console.warn('[WsTransport] server error:', msg.code);
					break;
				default:
					console.debug('[WsTransport] unknown frame type:', msg.type);
			}
		});

		this._ws.addEventListener('close', (event) => {
			this._ready = false;
			console.info('[WsTransport] closed:', event.code, event.reason);
		});

		this._ws.addEventListener('error', () => {
			this._ready = false;
			console.warn('[WsTransport] connection error');
		});
	}

	/** Close the WebSocket connection. */
	disconnect() {
		if (this._ws) {
			this._ws.close();
			this._ws    = null;
			this._ready = false;
		}
	}

	/**
	 * Delegates to AjaxTransport — the Worker is receive-only.
	 * @param {Object} payload
	 * @returns {Promise}
	 */
	send(payload) {
		return this._ajax.send(payload);
	}

	/**
	 * True once the Worker has confirmed auth_ok.
	 * @returns {boolean}
	 */
	isConnected() {
		return this._ready;
	}
}
